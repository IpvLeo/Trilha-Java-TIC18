
public class Itens {

    String descricao;
    int id;
    double preco;

    

    
    public Itens(String descricao, int id, double preco) {
        this.descricao = descricao;
        this.id = id;
        this.preco = preco;
    }


    public String getDescricao() {
        return descricao;
    }

    







}
